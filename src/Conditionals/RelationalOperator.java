package Conditionals;

public class RelationalOperator {
    public static void main(String[] args) {
        int a = 3;
        int b = 4;

        int c = 0b1010;
        int d = 10;

        char ch = 'A';
        char ch1 = 65;

        System.out.println(a == b);
        System.out.println(ch == ch1);
        System.out.println(c == d);
        System.out.println(a != b);

    }
}
