package Basics;

public class modulusOperator {
    public static void main(String[] args) {

        System.out.println(65 % 23); // gives remainder in output mean
        System.out.println(10 % 23); // rule a%b = a [if a is less than b mean a<b]
        System.out.println(65 % -23); // a % -b = a % b
        System.out.println(-65 % 23); // -(a % b)
        System.out.println(-65 % -23); // -(a % b)

    }
}
