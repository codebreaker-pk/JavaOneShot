package Basics;

public class UpdatingVariables {
    public static void main(String[] args) {
        int x = 7;
        System.out.println(x);
        x = 10; //10 would be print
        System.out.println(x);
        x = x + 5; //15 would be print
        System.out.println(x);
        x = x + 15; //30 would be print
        System.out.println(x);
    }
}
