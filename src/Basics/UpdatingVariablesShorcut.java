package Basics;

public class UpdatingVariablesShorcut {

    public static void main(String[] args) {
        int x = 12;
        System.out.println(x);
        x += 12;// x = x +12
        System.out.println(x); //24 would be print

        x += 6;
        System.out.println(x); // 30 would be printed
    }
}
